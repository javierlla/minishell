#include "minishell.h"

int	is_digit_signal(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!ft_isdigit(str[i]) && str[i] != '-' && str[i] != '+')
			return (0);
		i += 1;
	}
	return (1);
}
